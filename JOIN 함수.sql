--조인은 여러번 들어갈 수 있음
SELECT E.FIRST_NAME, 
       D.DEPARTMENT_NAME, 
       L.CITY 
FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
LEFT JOIN LOCATIONS L ON D.LOCATION_ID = L.LOCATION_ID
ORDER BY E.FIRST_NAME DESC;
                          
--SELF JOIN
SELECT * FROM EMPLOYEES;

